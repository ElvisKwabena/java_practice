import java.lang.*;
import java.util.Scanner;

class switchcase
{
    public static void main(String[] args)
    {
        int a=2;

        switch(a)       
        {
            case 1: System.out.println("You had an A");
            break;
            case 2: System.out.println("You had an B");
            break;
            case 3: System.out.println("You had a C");
            break;
            default: System.out.println("Woboa koraa");
        }

    }

}